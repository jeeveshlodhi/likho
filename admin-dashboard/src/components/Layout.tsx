import { useState } from 'react';
import { NavLink, Outlet, useLocation } from 'react-router';
import {
  LayoutDashboard,
  Flag,
  Settings,
  MessageSquare,
  Package,
  Menu,
  X,
  RefreshCw,
} from 'lucide-react';
import { cn } from '@/lib/utils';

